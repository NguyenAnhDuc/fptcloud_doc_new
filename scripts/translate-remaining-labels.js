function apply(file) {
  let c = fs.readFileSync(file, 'utf-8');
  let count = 0;
  for (const [from, to] of Object.entries(map)) {
    const escaped = from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const re = new RegExp('"' + escaped + '"', 'g');
    const m = c.match(re);
    if (m) { count += m.length; c = c.replace(re, '"' + to + '"'); }
  }
  if (count > 0) fs.writeFileSync(file, c);
  return count;
}

